def sumar(numero1:int, numero2:int)->int:
    resultado=numero1+numero2
    return resultado

def restar(numero1, numero2):
    resultado=numero1-numero2
    return resultado

def multiplicar(numero1, numero2):
    resultado=numero1*numero2
    return resultado

def dividir(numero1, numero2):
    resultado=numero1/numero2
    return resultado


